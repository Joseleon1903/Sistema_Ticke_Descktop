    public void PonerFecha() {
        DateFormat fm = new SimpleDateFormat("MM/dd/yyyy");
        String Date = fm.format(new java.util.Date());
        LabelNowDay.setText("" + Date);

    }

    private void LimpiarCampos() {
        TxtTelefono.setText("");
        TxtNombre.setText("");
        TxtModelo.setText("");
        TxtCedula.setText("");
        TxtObservaciones.setText("");
        TxtSerie.setText("");
    }
